const sql = require("./db.js")

const Tipo1 = function (record) {
  const keys = Object.keys(record)
  keys.map(key => this[key] = record[key])
}

Tipo1.create = (newRecord, result) => {
  sql.query("INSERT INTO tipo1 SET ?", newRecord, (err, res) => {
    if (err) {
      console.log("error: ", err)
      result(err, null)
      return
    }

    result(null, { id: res.insertId, ...newRecord })
  })
}

Tipo1.findById = (id, result) => {
  sql.query(`SELECT * FROM tipo1 WHERE id = ${id}`, (err, res) => {
    if (err) {
      console.log("error: ", err)
      result(err, null)
      return
    }

    if (res.length) {
      result(null, res[0])
      return
    }

    result({ kind: "not_found" }, null)
  })
}

Tipo1.getAll = result => {
  sql.query("SELECT ID, TIPCOD, SUBTIPCOD, SUBTIPDES FROM tipo1", (err, res) => {
    if (err) {
      console.log("error: ", err)
      result(err, null)
      return
    }

    result(null, res)
  })
}

Tipo1.updateById = (id, record, result) => {
  const fields = []
  const values = []
  Object.keys(record).filter(field => field != 'ID').map(field => {
    fields.push(`${field} = ?`)
    values.push(record[field])
  })
  values.push(id)
  sql.query(`UPDATE tipo1 SET ${fields.join(',')}  WHERE ID = ?`,
    values,
    (err, res) => {
      if (err) {
        console.log("error: ", err)
        result(err, null)
        return
      }

      if (res.affectedRows == 0) {
        result({ kind: "not_found" }, null)
        return
      }

      result(null, { id: id, ...record })
    }
  )
}

Tipo1.remove = (id, result) => {
  sql.query("DELETE FROM tipo1 WHERE id = ?", id, (err, res) => {
    if (err) {
      console.log("error: ", err)
      result(err, null)
      return
    }

    if (res.affectedRows == 0) {
      result({ kind: "not_found" }, null)
      return
    }

    result(null, res)
  })
}

Tipo1.removeAll = result => {
  sql.query("DELETE FROM tipo1", (err, res) => {
    if (err) {
      console.log("error: ", err)
      result(err, null)
      return
    }

    result(null, res)
  })
}

module.exports = Tipo1